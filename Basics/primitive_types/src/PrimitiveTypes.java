public class PrimitiveTypes {

    public static void main(String[] args) {

//        Tipos primitivos Enteros
        byte byteNumber = 9;
        short shortNumber = 500;
        int intNumber = 12453;
        long longNumber = 912349827;

//        Tipos primitivos Flotantes
        float floatNumber = 4.5f;
        double doubleNumber = 234.34234234;

//        Tipos primitivos Char
        char charText = 'd';
        char charTextTwo = '\u0021';
        char charTextThree = 33;

//        Tipos primitivos Boolean
        boolean booleanData = true;
    }
}
